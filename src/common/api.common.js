const axios = require('axios')

const ApiCommon = {
	checkServerStatus () {
		var serverStatus = false
		axios.get('/api/status').then(
			result => {
				if (result.status === 200) serverStatus = true
				return serverStatus
			},
			error => {
				console.error(error)
				serverStatus = false
				return serverStatus
			}
		)
	}
}

export default ApiCommon